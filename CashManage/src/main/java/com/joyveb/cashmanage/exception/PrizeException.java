package com.joyveb.cashmanage.exception;

public class PrizeException extends Exception {

	public PrizeException() {
		super();
	}

	public PrizeException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrizeException(String message) {
		super(message);
	}

	public PrizeException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 6638010560290942132L;

}
