package com.joyveb.cashmanage.entity;


public class TransferExcel{

	private String transfertime;
	private String transfertype;
	private String count;
	private String transferamount;
	private String factorage;
	private String xiane;
	public String getTransfertime() {
		return transfertime;
	}
	public void setTransfertime(String transfertime) {
		this.transfertime = transfertime;
	}
	public String getTransfertype() {
		return transfertype;
	}
	public void setTransfertype(String transfertype) {
		this.transfertype = transfertype;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getTransferamount() {
		return transferamount;
	}
	public void setTransferamount(String transferamount) {
		this.transferamount = transferamount;
	}
	public String getFactorage() {
		return factorage;
	}
	public void setFactorage(String factorage) {
		this.factorage = factorage;
	}
	public String getXiane() {
		return xiane;
	}
	public void setXiane(String xiane) {
		this.xiane = xiane;
	}
	
	
}